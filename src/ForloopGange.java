public class ForloopGange {

    public static void main(String[] args) {

        for (int i = 1; i <= 20; i++) {
            int result = i * 2;
            System.out.println(i + " * 2 = " + result);

        }
        System.out.println();
        for (int i = 1; i <= 20; i++) {
            int result = i * 2;
            System.out.println(i + result);

        }
    }
}

